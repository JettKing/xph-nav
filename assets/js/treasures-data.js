/* =========================================================
   📌 新增资源模板｜使用说明

   【一、如何新增资源】

   1. 复制下面完整的一组资源代码：
      从开头的 {
      一直到结尾的 },

   2. 将复制的代码粘贴到资源列表中。

   3. 修改：
      icon      = 资源图标
      name      = 资源名称
      desc      = 资源简介
      category  = 所属分类
      tag       = 显示标签
      website   = 官网地址
      project   = 项目地址

   ---------------------------------------------------------

   【二、符号使用说明】

   {  = 一条资源数据的开始

   }  = 一条资源数据的结束

   ,  = 分隔不同字段或不同资源

   "  = 文字内容必须放在英文双引号里面

   ---------------------------------------------------------

   【三、项目地址填写规则】

   有 GitHub / 项目地址：

   project:"https://github.com/xxx/xxx"

   页面自动显示：
   「项目地址」

   没有项目地址：

   project:""

   注意：
   必须保留英文双引号 ""
   不要删除 project 字段。

   页面会自动显示：
   「暂无项目」

   ---------------------------------------------------------

   【四、添加多条资源时】

   每条资源都必须使用：

   {
   ...
   },

   例如：

   {
   第一条资源
   },

   {
   第二条资源
   },

   {
   第三条资源
   }

   最后一条资源后面的逗号可以不写。

   ---------------------------------------------------------

   【五、category 分类规则】

   category:"xxx"

   xxx 必须填写当前页面已经存在的分类标识，
   并且必须与 HTML 页面中的：

   data-category="xxx"

   完全一致，否则分类筛选无法正常工作。

   ---------------------------------------------------------

   【六、重要注意事项】

   1. 所有符号必须使用英文符号。
   2. 不要删除 { }。
   3. 每个字段之间必须使用英文逗号 , 分隔。
   4. 文字和链接必须放在英文双引号 "" 中。
   5. 没有项目地址时填写 project:""。
   6. 新增资源时必须放在 const xxxList = [ ... ]; 的 [ ] 内。
   7. 不要把资源添加到 ]; 后面。
   8. 修改完成后注意检查上一条资源结尾是否有英文逗号 ,。

========================================================= */


